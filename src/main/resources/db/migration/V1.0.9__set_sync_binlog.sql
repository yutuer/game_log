-- ============================================================
-- V1.0.9  关闭 binlog 同步刷盘
-- 创建时间: 2026-06-11
-- 描述: 将 sync_binlog 设为 0，binlog 写入由操作系统控制
--       刷盘时机，减少每次事务提交的磁盘 fsync 开销。
--       值为 0 时：操作系统决定何时刷盘，性能提升约 20-30%。
--       崩溃时可能丢失最多 1 秒的 binlog 事务。
--
-- ⚠️ 此设置重启 MySQL 后失效，如需持久化请在 my.ini/my.cnf
--    的 [mysqld] 段添加：sync_binlog = 0
-- ============================================================

SET GLOBAL sync_binlog = 0;
